/**
 * 
 * <h1>Interfaces</h1>
 * 
 * São prótotipo de classes que devem se nomeadas com {@link IdentificadoresVálidos}
 * <br/>
 * Não Possuem Construtores e não podem se Instanciadas a não ser de forma anonima {@link ClasseAnonima}
 * <br/>
 * Metodos não possuem corpo e são automaticamente convertidos em public abstract durante a compilação
 * <br/>
 * Metodos de uma interface não podem ser estaticos
 * <br/>

 * Atributos são convertidos para constantes recebendo (public static final) durante a compilação
 * <br/>
 * <br/>
 * <h2>Estrutura para criação de interfaces a palavra 'interface' por obrigatoriedade.</h2><br/>
 * <br/>
 * Elementos entre colchetes são opcionais :<br/>
 * <br/>
 * [modificadores] interface NomeInterface<br/>
 * [extends SuperInterface1,SuperInterface2, ...]{<br/>
 * [constantes]<br/>
 * [metodos sem corpo]<br/>
 * }<br/>
 * <br/>
 * @author Saymon
 *
 */

